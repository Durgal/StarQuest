        jTextCommand.addKeyListener(new KeyAdapter() {
            @Override
            public void keyPressed(KeyEvent e) {

                // Only process if input is enabled
                if (!getInputEnabled()) {
                    e.consume();
                    return;
                }

                // Check for Enter (without Shift)
                if (e.getKeyCode() == KeyEvent.VK_ENTER && !e.isShiftDown()) {
                    e.consume(); // prevent newline

                    // Get trimmed input
                    String input = jTextCommand.getText().trim();

                    // If input is empty, do nothing
                    if (input.isEmpty()) {
                        return;
                    }

                    // Clear the command box
                    jTextCommand.setText("");

                    // Example: echo command in story panel
                    System.out.println("You entered: " + input);
                    Terminal.showOutput(Quest.start(input),jTextStory,jTextCommand);
                }
            }
        });

